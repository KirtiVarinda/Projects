public class MainPageBeanClass {

    private String city_id,city_name,city_cprice,city_uid,city_count,city_status;

    public String getCity_id() {
        return city_id;
    }

    public void setCity_id(String city_id) {
        this.city_id = city_id;
    }

    public String getCity_name() {
        return city_name;
    }

    public void setCity_name(String city_name) {
        this.city_name = city_name;
    }

    public String getCity_cprice() {
        return city_cprice;
    }

    public void setCity_cprice(String city_cprice) {
        this.city_cprice = city_cprice;
    }

    public String getCity_uid() {
        return city_uid;
    }

    public void setCity_uid(String city_uid) {
        this.city_uid = city_uid;
    }

    public String getCity_count() {
        return city_count;
    }

    public void setCity_count(String city_count) {
        this.city_count = city_count;
    }

    public String getCity_status() {
        return city_status;
    }

    public void setCity_status(String city_status) {
        this.city_status = city_status;
    }
}
